integer_number = 5
float_number = 10.0
complex_number = 1j+1

print(type(integer_number))  # output: <class 'int'>
print(type(float_number))  # output: <class 'float'>
print(type(complex_number))  # output: <class 'complex'>

print(integer_number // 0.33)  # output: 15.0
print(float_number % 3.0)  # output: 1.0
print(complex_number ** 0.5)  # output: (1.0986841134678098+0.45508986056222733j)
