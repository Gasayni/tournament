my_tuple = tuple('hello, world!')
print(type(my_tuple))  # output: <class 'tuple'>
print(my_tuple)  # output: ('h', 'e', 'l', 'l', 'o', ',', ' ', 'w', 'o', 'r', 'l', 'd', '!')

my_tuple = ('hello, world!')
print(type(my_tuple))  # output: <class 'str'>
print(my_tuple)  # output: hello, world!

my_tuple = ('hello, world!',)
print(type(my_tuple))  # output: <class 'tuple'>
print(my_tuple)  # output: ('hello, world!',)
